    private void get_Class(String check) {
        Class_Controller CC = new Class_Controller(conn);
        LClass = CC.select(check);
    }

    private void set_Class() {
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        for (tbl_Class c : LClass) {
            dcbm.addElement(c.getName());
        }
        Class_Student.setModel(dcbm);
        for (int i = 0; i < LClass.size(); i++) {
            tbl_Class c = LClass.get(i);
            if (c.getId() == student.getClass_ID()) {
                Class_Student.setSelectedIndex(i);
            }
        }
    }

    private void loadform() {
        MaSV.setText(student.getMasv());
        Name.setText(student.getName());
        Phone.setText(student.getPhone());
        Email.setText(student.getEmail());
        Address.setText(student.getAddress());
        try {
            DOB.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(student.getDOB()));
        } catch (ParseException ex) {
            Logger.getLogger(Edit_Student.class.getName()).log(Level.SEVERE, null, ex);
        }
        switch (student.getGender()) {
            case 1:
                Nam.doClick();
                break;
            case 2:
                Nu.doClick();
                break;
            default:
                unisex.doClick();
                break;
        }
        if (student.getStatus() == 1) {
            now.doClick();
        } else {
            unow.doClick();
        }
    }
    
        private boolean check_Masv(String masv) {
        Student_Controller SC = new Student_Controller(conn);
        List<tbl_Student> LS_check = SC.select(" WHERE MaSV != N'" + student.getMasv() + "'");
        boolean check = false;
        for (tbl_Student s : LS_check) {
            if (masv.equals(s.getMasv())){
                check = true;
            }
        }
        return check;
    }

    private boolean check_Mail(String email) {
        Student_Controller SC = new Student_Controller(conn);
        List<tbl_Student> LS_check = SC.select(" WHERE Email != N'" + student.getEmail() + "'");
        boolean check = false;
        for (tbl_Student s : LS_check) {
            if (email.equals(s.getEmail())){
                check = true;
            }
        }
        return check;
    }
    
    private boolean check_Phone(String phone) {
        Student_Controller SC = new Student_Controller(conn);
        List<tbl_Student> LS_check = SC.select(" WHERE Phone != N'" + student.getPhone()+ "'");
        boolean check = false;
        for (tbl_Student s : LS_check) {
            if (phone.equals(s.getPhone())){
                check = true;
            }
        }
        return check;
    }

