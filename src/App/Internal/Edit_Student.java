    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        String regex_Phone = "^0[0-9]{9,10}$";
        int id = student.getId();
        String masv = MaSV.getText();
        String name = Name.getText();
        String phone = Phone.getText();
        String mail = Email.getText();
        String address = Address.getText();
        String birthday = new SimpleDateFormat("yyyy/MM/dd").format(DOB.getDate());
        int gender = 1;
        if (Nam.isSelected()) {
            gender = 1;
        } else if (Nu.isSelected()) {
            gender = 2;
        } else if (unisex.isSelected()) {
            gender = 3;
        }
        int class_choose = LClass.get(Class_Student.getSelectedIndex()).getId();
        int status;
        if (now.isSelected()) {
            status = 1;
        } else {
            status = 2;
        }
        if (check_Masv(masv)) {
            JOptionPane.showMessageDialog(null, "Mã SV bị trùng");
        } else if (check_Mail(mail)) {
            JOptionPane.showMessageDialog(null, "Email đã được sử dụng!");
        } else if (ev.isNotValidEmail(mail)) {
            JOptionPane.showMessageDialog(null, "Hãy kiểm tra lại email!");
        } else if (check_Phone(phone)) {
            JOptionPane.showMessageDialog(null, "SĐT đã được sử dụng!");
        } else if (name.length() == 0 || phone.length() == 0 || mail.length() == 0 || address.length() == 0 || masv.length() == 0) {
            JOptionPane.showMessageDialog(null, "Hãy điền đầy đủ thông tin");
        } else if (phone.length() > 13 || phone.length() < 8 || regex_Phone.matches(phone)) {
            JOptionPane.showMessageDialog(null, "SĐT phải từ 8 đến 13 số");
        } else {
            tbl_Student student_edit = new tbl_Student(id, masv, name, phone, mail, address, birthday, status, class_choose, "", gender);
            Student_Controller SC = new Student_Controller(conn);
            if (SC.update(student_edit)) {
                JOptionPane.showMessageDialog(null, "Bạn đã thay đổi thông tin sinh viên thành công!");
                load.load_table();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Đã có lỗi xảy ra");
            }
        }
    }//GEN-LAST:event_UpdateActionPerformed

